#include "ti86asm.inc"

;******** $8200 offsets ********
xPos		.equ	0
yPos		.equ	xPos+1
xDest		.equ	yPos+1
yDest		.equ	xDest+1
level		.equ	yDest+1
board		.equ	level+1
score		.equ	board+1
keys		.equ	score+2
energy	.equ	keys+1
lives		.equ	energy+1
shift		.equ	lives+1
levelArray	.equ	shift+1


;******** Program ********

.org _asm_exec_ram

	nop
	jp start
	.dw $0000
	.dw Title

Title:
	.db	"Elements 1.0",0

start:
	call _runindicoff
	call _flushallmenus
	ld	ix,$8200		; initialise IX so we can use our offsets
	xor a
	ld	(ix+level),a
mainMenu:
	xor a
	ld	(ix+score),a
	ld	(ix+score+1),a
	ld	(ix+lives),3
	ld	(ix+shift),a
showMenu:
	call _clrLCD
	ld	de,titlePic
	ld	hl,$FC00+4
	ld c,10
sM1:
	ld b,8
sMLoop:
	ld a,(de)
	ld (hl),a
	inc hl
	inc de
	djnz sMLoop
	push de
	ld de,8
	add hl,de
	pop de
	dec c
	jr nz,sM1

	ld	hl,menuURL
	ld	bc,$0a0b
	call	putString

	ld	bc,$1217
	call	putString

	ld	bc,$1800
	call	putString
	ld	a,(ix+level)
	inc	a
	call DispA
	ld	hl,menuExit
	ld	bc,$1e15
	call	putString

	ld	bc,$2411
	call	putString

	ld	bc,$2a29
	call	putString

	ld	bc,$3214
	call	putString
	ld	hl,(highScore)
	call DispHL
	ld	b,$50
	call	bigDelay

menuLoop:
	call	resetKeyport
	ld	a,$fe
	out	(1),a
	in	a,(1)
	cp	253			; left
	jr	z,prevLevel
	cp	251			; right
	jr	z,nextLevel
	call	resetKeyport
	ld	a,$bf
	out	(1),a
	in	a,(1)
	cp	223			; 2nd
	jr	z,startLevel
	cp	127			; MORE
	jr	z,delHighScore
	cp 191			;EXIT
	jp z,Quit
	jp	menuLoop

nextLevel:
	ld	a,(highLevel)
	cp	(ix+level)
	jr	z,menuLoop
	inc	(ix+level)
	jp	showMenu

prevLevel:
	xor	a
	cp	(ix+level)
	jr	z,menuLoop
	dec	(ix+level)
	jp	showMenu

delHighScore:
	ld	hl,confirmDelete
	ld	bc,$3900
	ld	(ix+shift),1
	call	putMessage
	bit	0,(ix+shift)
	jp	nz,showMenu
	ld	(ix+shift),0
	ld	hl,0
	ld	(highScore),hl
	ld	a,0
	ld	(highLevel),a
	ld	(ix+level),a
	jp	showMenu

startLevel:
	ld	(ix+board),0
	ld	(ix+keys),0
	ld	(ix+energy),25
	bit	0,(ix+shift)
	jr	nz,noAddPoints
	call	add25points
	call	add25points
noAddPoints:
	ld	(ix+shift),0

startBoard:
	ld	a,(ix+level)
	ld	hl,levels
	ld	ix,boardLengths
	ld	de,screenLengths
	cp 0
	jr	z,level0
	ld	b,a
loadLoop1:
	push	bc
	ld	b,(ix)
	inc	ix
	call	skipScreen
	pop	bc
	djnz	loadLoop1
level0:

	ld	a,($8200+board)
	cp	0
	jr	z,board0
	ld	b,a
	call	skipScreen
board0:

	call	getNextByte
	ld	($8200+xPos),a
	ld	($8200+xDest),a
	call	getNextByte
	ld	($8200+yPos),a
	ld	($8200+yDest),a
	ld	ix,$8200+levelArray
	ld	a,(de)
	ld	b,a
decompressLoop:
	push	bc
	call	getNextByte
	ld	c,a
	and	%00000111
	inc	a
	ld	b,a
	srl	c
	srl	c
	srl	c
writeRun:
	ld	(ix),c
	inc	ix
	djnz	writeRun
	pop	bc
	djnz	decompressLoop

displayBoard:
	set textwrite,(iy+new_grf_flgs)
	ld hl,_plotSScreen
	ld de,_plotSScreen+1
	ld (hl),0
	ld bc,1023
	ldir
	ld	e,0
	ld	hl,$8200+levelArray
	ld	b,10

dispLoop1:
	push	bc
	ld	b,19
	ld	d,0

dispLoop2:
	push	bc

	ld	a,(hl)
	inc	hl
	ld	b,a
	push	de
	push	hl
	call	putBlock
	pop	hl
	pop	de
	inc	d
	pop	bc
	djnz	dispLoop2

	inc	e
	pop	bc
	djnz	dispLoop1

	ld	hl,status
	ld	bc,$3200
	call	putString

	ld	bc,$321b
	call	putString

	call	dispScore

	ld	hl,nrg
	ld	bc,$3900
	call	putString

	ld	bc,$393e
	call	putString

	ld	a,(ix+level)
	inc	a
	add a,'0'
	call _vputmap
	ld	ix,$8200

	ld	hl,dash
	ld	bc,$3957
	call	putString

	ld	a,(ix+board)
	inc	a
	add a,'0'
	call _vputmap
	ld	ix,$8200

	ld	(ix+shift),1
	bit	0,(ix+keys)
	jr	z,noLightKey
	ld	b,14
	ld	d,3
	ld	e,10
	call	putBlock
noLightKey:
	bit	1,(ix+keys)
	jr	z,noDarkKey
	ld	b,13
	ld	d,4
	ld	e,10
	call	putBlock
noDarkKey:

	ld	a,(ix+lives)
	cp	0
	jr	z,noLives
	cp	6
	jr	c,notTooMany
	ld	a,5
notTooMany:
	ld	d,18
	ld	e,10
	ld	b,a
livesLoop:
	push	bc
	ld	b,25
	push	de
	call	putBlock
	pop	de
	dec	d
	pop	bc
	djnz	livesLoop
noLives:
	ld	(ix+shift),0

	ld	a,(ix+energy)
	cp	0
	jr	z,doneEnergy
	cp	36
	jr	c,oneEnergyBar
	ld	hl,_plotSScreen+949
	ld	b,35
	call	horizLine
	ld	hl,_plotSScreen+965
	ld	b,35
	call	horizLine
	ld	hl,_plotSScreen+997
	ld	a,(ix+energy)
	sub	35
	cp	36
	jr	c,notTooLong
	ld	a,35
notTooLong:
	ld	b,a
	push	bc
	call	horizLine
	pop	bc
	ld	hl,_plotSScreen+1013
	call	horizLine
	jr	doneEnergy

oneEnergyBar:
	ld	b,(ix+energy)
	ld	hl,_plotSScreen+949
	call	horizLine
	ld	b,(ix+energy)
	ld	hl,_plotSScreen+965
	call	horizLine
doneEnergy:

	call	putPlayer
	res textwrite,(iy+new_grf_flgs)

	ld	hl,outOfEnergy
	ld	a,(ix+energy)
	cp	0
	jp	z,die
	cp	253
	jp	nc,die

	ld hl,_plotSScreen
	ld de,$FC00
	ld bc,1024
	ldir

	call	delay

gameLoop:
	call	resetKeyport
	ld	a,$fe
	out	(1),a
	in	a,(1)
doubleMove:
	cp	254			; down
	jr	z,down
	cp	253			; left
	jr	z,left
	cp	251			; right
	jr	z,right
	cp	247			; up
	jr	z,up
	call	resetKeyport
	ld	a,$bf
	out	(1),a
	in	a,(1)
	cp	191			; EXIT
	jp	z,endGame
	ld	hl,suicide
	cp	127			; MORE
	jp	z,die
	jr	gameLoop

down:
	inc	(ix+yDest)
	jr	checkDest
left:
	ld	a,(ix+xPos)
	cp	0
	jr	z,gameLoop
	dec	(ix+xDest)
	jr	checkDest
right:
	ld	a,(ix+xPos)
	cp	18
	jr	nz,notNextBoard
	inc	(ix+board)
	jp	startBoard
notNextBoard:
	inc	(ix+xDest)
	jr	checkDest
up:
	dec	(ix+yDest)

checkDest:
	call	getElementAddress
	ld	c,(hl)
	ld	hl,destCases
	sla	c
	ld	b,0
	add	hl,bc
	ld	e,(hl)
	inc	hl
	ld	d,(hl)
	ex	de,hl
	jp	(hl)

dNothing:
	inc	(ix+board)
	jp	startBoard

dWall:
	ld	a,(ix+xPos)
	ld	(ix+xDest),a
	ld	a,(ix+yPos)
	ld	(ix+yDest),a
	jp 	displayBoard

dExit:
	dec	(ix+energy)
	ld	a,(ix+energy)
	cp	0
	jr	z,noBonus
	ld	b,a
addBonus:
	call	add25points
	djnz	addBonus
noBonus:
	call	putPlayer
	ld	bc,$3242
	ld	(_penCol),bc
	call	dispScore
	ld	hl,boardComplete
	ld	a,(ix+level)
	cp	6
	jr	nz,endBoardMessage
	ld	hl,gameComplete
endBoardMessage:
	ld	bc,$3900
	call	putMessage

	ld	a,(ix+level)
	cp	6
	jp	z,endGame

	inc	(ix+level)
	ld	a,(highLevel)
	cp	(ix+level)
	jr	nc,sameHighLevel
	inc	a
	ld	(highLevel),a
sameHighLevel:
	ld	(ix+board),0
	jp 	startLevel

dFloor:
	ld	a,(ix+xDest)
	ld	(ix+xPos),a
	ld	a,(ix+yDest)
	ld	(ix+yPos),a
	dec	(ix+energy)
	jp	displayBoard

dLeft:
	ld	a,(ix+xPos)
	cp	(ix+xDest)
	jp	z,dWall
	jp	c,dWall
	jr	followArrow

dRight:
	ld	a,(ix+xPos)
	cp	(ix+xDest)
	jp	nc,dWall
	jr	followArrow

dUp:
	ld	a,(ix+yPos)
	cp	(ix+yDest)
	jp	z,dWall
	jp	c,dWall
	jr	followArrow

dDown:
	ld	a,(ix+yPos)
	cp	(ix+yDest)
	jp	nc,dWall
followArrow:
	dec	(ix+energy)
	jp	doubleMove

dShard:
	ld	d,2
	call	writeArray
	call	add25points
	call	add25points
	jr	dFloor

dEnergy:
	ld	d,2
	call	writeArray
	ld	a,(ix+energy)
	add	a,26
	ld	(ix+energy),a
	jr	dFloor

d1up:
	ld	d,2
	call	writeArray
	inc	(ix+lives)
	jp	dFloor

dKeyDark:
	set	1,(ix+keys)
	ld	d,2
	call	writeArray
	jp	dFloor

dKeyLight:
	set	0,(ix+keys)
	ld	d,2
	call	writeArray
	jp	dFloor

dLockDark:
	bit	1,(ix+keys)
	jp 	z,dWall
	ld	d,2
	call	writeArray
	ld	a,17
	ld	c,2
	call	replace
	res	1,(ix+keys)
	jp	dFloor

dLockLight:
	bit	0,(ix+keys)
	jp 	z,dWall
	ld	d,2
	call	writeArray
	ld	a,18
	ld	c,2
	call	replace
	res	0,(ix+keys)
	jp	dFloor

dSwitchLeft:
	ld	d,20
	jr	switchArrows

dSwitchRight:
	ld	d,19
switchArrows:
	call	writeArray
	dec	(ix+energy)
	ld	a,6
	ld	c,25
	call	replace
	ld	a,7
	ld	c,6
	call	replace
	ld	a,25
	ld	c,7
	call	replace
	ld	a,8
	ld	c,25
	call	replace
	ld	a,9
	ld	c,8
	call	replace
	ld	a,25
	ld	c,9
	call	replace
	jp 	dWall

dRound:
	dec	(ix+energy)
	ld	a,17
	ld	c,25
	call	replace
	ld	a,18
	ld	c,17
	call	replace
	ld	a,25
	ld	c,18
	call	replace
	jp 	dWall

dSquare:
	dec	(ix+energy)
	ld	a,24
	ld	c,2
	call	replace
	jp 	gameLoop

dHole:
	ld	a,(ix+level)
	cp	6
	jr	nz,fallInHole
	ld	a,(ix+board)
	cp	4
	jr	c,fallInHole
	ld	hl,teleporters
	sub	4
	sla	a
	sla	a
	sla	a
	ld	b,0
	ld	c,a
	add	hl,bc

	ld	b,2
checkTeleporters:
	call	getNextByte
	cp	(ix+xDest)
	jr	nz,checkOther2
	call	getNextByte
	cp	(ix+yDest)
	jr	nz,checkOther1
	call	getNextByte
	ld	(ix+xDest),a
	call	getNextByte
	ld	(ix+yDest),a
	dec	(ix+energy)
	dec	(ix+energy)
	jp	dFloor
checkOther1:
	inc	hl
checkOther2:
	inc	hl
	inc	hl
	inc	hl
	djnz	checkTeleporters

fallInHole:
	call	putPlayer
	ld	hl,youHaveDied
die:
	ld	bc,$3906
	call	putMessage

	ld	hl,$FC00
	ld	de,$FC00+(16*62)
	ld	b,31
blindLoop:
	push	bc
	ld	a,%00000000
	call	drawRow
	push	hl
	ld	a,%11111111
	call	drawRow
	pop	hl
	ex	de,hl
	push	hl
	ld	a,%11111111
	call	drawRow
	ld	a,%00000000
	call	drawRow
	pop	hl
	ld	bc,16
	scf
	ccf
	sbc	hl,bc
	ex	de,hl
	push	de
	push	hl
	call	delay
	call	delay
	call	delay
	call	delay
	pop	hl
	pop	de
	pop	bc
	djnz	blindLoop
	call _clrLCD

	ld	a,(ix+lives)
	cp	0
	jr	z,endGame
	dec	(ix+lives)
	jp 	startLevel
endGame:
	ld	de,(highScore)
	ld	hl,($8200+score)
	call	hiscr
	jp	nz,mainMenu
	ld	(highScore),hl
	ld	hl,newHighScore
	ld	bc,$3902
	call	putMessage
	jp	mainMenu


getNextByte:
	ld	a,(hl)
	inc	hl
	ret


drawRow:
	ld	b,16
drawRowLoop:
	ld	(hl),a
	inc	hl
	djnz	drawRowLoop
	ret


replace:			; replace all A's with C's
	ld	hl,$8200+levelArray
	ld	b,190
replaceLoop:
	cp	(hl)
	jr	nz,noMatch
	ld	(hl),c
noMatch:
	inc	hl
	djnz	replaceLoop
	ret


writeArray:
	call	getElementAddress
	ld	(hl),d
	ret


getElementAddress:
	ld	a,(ix+yDest)
	sla	a
	sla	a
	sla	a
	sla	a
	add	a,(ix+yDest)
	add	a,(ix+yDest)
	add	a,(ix+yDest)
	add	a,(ix+xDest)
	ld	b,0
	ld	c,a
	ld	hl,$8200+levelArray
	add	hl,bc
	ret


putPlayer:
	ld	d,(ix+xPos)
	ld	e,(ix+yPos)
	push	ix
	ld	ix,player
	jr	drawSprite

putBlock:			; B = sprite type, D = x-pos, E= y-pos
	push	ix
	push	de
	ld	ix,sprites
	ld	a,b
	cp	0
	jr	z,gotSprite
	ld	de,5
getSprite:
	add	ix,de
	djnz	getSprite
gotSprite:
	pop	de
drawSprite:
	ld	a,e
	sla	a
	sla	a
	add	a,e
	ld	l,a
	ld	a,($8200+shift)
	cp	1
	jr	nz,noShift
	inc	l
noShift:
	ld	a,d
	sla	a
	sla	a
	add	a,d
	inc	a
	ld	b,5
	call	PutSprite
	pop	ix
	ret


add25points:
	ld	hl,($8200+score)
	ld	de,25
	add	hl,de
	ld	($8200+score),hl
	ret


dispScore:
	ld	hl,($8200+score)
	call DispHL
	ld	ix,$8200
