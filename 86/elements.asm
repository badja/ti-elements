putString:
	ld a,c
	add a,16
	ld c,a
	ld	(_penCol),bc
	call _vputs
	ret


horizLine:
	ld	c,%10000000
drawLine:			; draw a horizontal line
	ld	a,c		; C = initial bit pattern
	or	(hl)		; HL -> graph buffer byte containing start of line
	ld	(hl),a
	dec	b		; B = length of line
	ld	a,b
	cp	0
	ret	z
	srl	c
	jr	nc,drawLine
	inc	hl
	ld	c,%10000000
	ld	a,b
	srl	a
	srl	a
	srl	a
	jr	z,drawLine
lineLoop:
	ld	(hl),%11111111
	inc	hl
	dec	a
	jr	nz,lineLoop
	ld	a,b
	and	%00000111
	ret	z
	ld	b,a
	jr	drawLine


putMessage:
	push	bc
	push	hl
	ld	hl,$FC00+(16*57)
	ld	a,%11111111
	ld	b,7
clearStatus:
	push	bc
	call	drawRow
	pop	bc
	djnz	clearStatus
	pop	hl
	pop	bc

	set	textInverse,(iy+textflags)
	call	putString
	ld	hl,press2nd
	call _vputs
	res	textInverse,(iy+textflags)
	ld	bc,$a0
	call	bigDelay
waitLoop:
	call	resetKeyport
	ld	a,$bf
	out	(1),a
	in	a,(1)
	cp	223			; 2nd
	ret	z
	bit	0,(ix+shift)
	jr	z,waitLoop
	call	resetKeyport
	ld	a,$ef
	out	(1),a
	in	a,(1)
	cp	254			; check if 0 is pressed
	jr	nz,waitLoop
	ld	(ix+shift),0
	ret


delay:
	ld	b,0
delayLoop:
	res	7,(ix+keys)
	djnz	delayLoop
	ret


bigDelay:
	push	bc
	call	delay
	pop	bc
	djnz	bigDelay
	ret


resetKeyport:
	ld	a,$ff			; reset keyport
	out	(1),a
	ret


skipScreen:
	push	bc
	ld	a,(de)
	inc	de
	inc	a
	inc	a
	ld	b,0
	ld	c,a
	add	hl,bc
	pop	bc
	djnz	skipScreen
	ret



;-----> Draw a sprite
; b=size of sprite
; l=yc
; a=xc
; ix holds pointer

PutSprite:
		ld h,a
		ld a,b
		ld (label1),a
		ld (label2),a
		ld a,h
		add a,16
		ld e,l
       push    ix              ; Save sprite address

;лллл   Calculate the address in graphbuf   лллл

	ld      hl,0            ; Do y*12
	ld      d,0
	ex      de,hl
	add     hl,hl
	add     hl,hl
	add     hl,hl
	add     hl,hl

	ld      d,0             ; Do x/8
	ld      e,a
	srl     e
	srl     e
	srl     e
	add     hl,de

	ld      de,_plotSScreen
	add     hl,de           ; Add address to graphbuf

	ld      b,00000111b     ; Get the remainder of x/8
	and     b
	cp      0               ; Is this sprite aligned to 8*n,y?
	jr      z,ALIGN


;лллл   Non aligned sprite blit starts here   лллл

	pop     ix              ; ix->sprite
	ld      d,a             ; d=how many bits to shift each line

	ld      e,8             ; Line loop

label1 = $-1

LILOP:  ld      b,(ix+0)        ; Get sprite data

	ld      c,0             ; Shift loop
	push    de
SHLOP:  srl     b
	rr      c
	dec     d
	jr      nz,SHLOP
	pop     de

	ld      a,b             ; Write line to graphbuf
	xor     (hl)
	ld      (hl),a
	inc     hl
	ld      a,c
	xor     (hl)
	ld      (hl),a

	ld      bc,15           ; Calculate next line address
	add     hl,bc
	inc     ix              ; Inc spritepointer

	dec     e
	jr      nz,LILOP        ; Next line

	jr      DONE1


;лллл   Aligned sprite blit starts here   лллл

ALIGN:                          ; Blit an aligned sprite to graphbuf
	pop     de              ; de->sprite
	ld      b,8

label2 = $-1

ALOP1:  ld      a,(de)
	xor      (hl)            ; xor=erase/blit
	ld      (hl),a
	inc     de
	push    bc
	ld      bc,16
	add     hl,bc
	pop     bc
	djnz    ALOP1

DONE1:
	ret



DispA:                                  ;Routine to Display A [By: Matthew Shepcar]
 ld l,a                                 ;Loads a into l
 ld h,0                                 ;0 into H
DispHL:                                 ;Displays HL [By: Matthew Shepcar]
 xor a                                  ;Loads 0 into A
 ld de,-1                               ;Loads -1 into DE
 ld (_curRow),de                        ;Loads de into CurRow
 call 4A33h                             ;Calls 4A33h
 dec hl                                 ;Decrease it
 jp _vputs                              ;Put on screen and return

Quit:
	call _clrLCD
	call _homeup
	ld hl,_asapvar
	rst 20h
	rst 10h
	ld a,b
	ld hl,highScore-_asm_exec_ram+4
	add hl,de
	adc a,0
	call _set_abs_dest_addr
	xor a
	ld hl,highScore
	call _set_abs_src_addr
	ld hl,3
	call _set_mm_num_bytes
	jp _mm_ldir





;---------= High Score =---------
; Input: de=previous high score
;	hl=current score
; Output: hl=high score
;	z=1 (a=0) if new high score, z=0 (a=1) if not
; Registers destroyed: af, de, hl
hiscr:	push	hl
	xor	a
	sbc	hl,de
